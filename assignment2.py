# research on IF....ELSE statements in python
# if...else statements are the primary tool for decision-making and controlling program flow. They allow your code to execute specific blocks based on whether a condition is True or False. 
#  Basic Structure
age = 20
if age >= 18:
    print("Eligible to vote.")
elif age > 13:
    print("Teenager, not yet eligible.")
else:
    print("Child.")
