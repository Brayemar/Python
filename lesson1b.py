# Python Data types
# String - It is a series of characters that are inside of quotes
name = "Joseph Kainembuga"
print(name)
print(type(name))

# Numeric 
# Int - This indicates a whole number in python. For an Integer, we dont use quotes
age = 39
print("The age of a person is: ", age)
print(type(age))

# float - this is a number with a decimal
coordinates = 15.4524
print("The coordinates are:", coordinates)
print(type(coordinates))


